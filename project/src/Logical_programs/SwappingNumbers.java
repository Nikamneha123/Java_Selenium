package Logical_programs;

public class SwappingNumbers {

	public static void main(String[] args) {
		
		int a=10,b=20;
		
		System.out.println("before swapping values:"+a+" "+b);
		
		 int C=a;
		 a=b;
		 b=C;
		 
		 System.out.println("AFTER swapping values:"+a+" "+b);
		
		
	}
	
	
	
	
	
	
}
